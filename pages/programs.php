<script>
function toggleEduModule(button) {
 const panel = button.nextElementSibling;
 const icon = button.querySelector('.edu-icon');
 
 if (panel.style.display === "block") {
  panel.style.display = "none";
  icon.innerText = "+";
 } else {
  panel.style.display = "block";
  icon.innerText = "−";
 }
}
</script>
